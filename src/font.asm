; ---------------------------------------------------------------
; 5x7 font, one glyph = 8 bytes (7 rows + 1 pad byte).
; Stored bottom row first because the text kernel walks Y from
; FONTH-1 down to 0. The GLYPH macro accepts rows in visual
; (top to bottom) order and reverses them.
;
; FontData MUST stay page aligned: the pointer math in
; TextBlock derives the high byte from code/32.
;
; Char codes: 0-9 = digits (hex friendly), 10-35 = A-Z,
; 36 = space, 37 = '-', 38 = '=', 39-42 = arrows up/down/left/right
; ---------------------------------------------------------------

; Each visual row is stored TWICE (double height): the 48px kernel
; must rewrite GRP0/GRP1 on every scanline, so the doubling lives in
; the font data, not in the kernel. 14 data rows + 2 pad = 16 bytes
; per glyph (power of two keeps the pointer math cheap).
        MAC GLYPH
        .byte {7},{7},{6},{6},{5},{5},{4},{4},{3},{3},{2},{2},{1},{1},0,0
        ENDM

